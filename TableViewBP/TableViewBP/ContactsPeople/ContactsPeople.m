+ (NSArray *)allContactsPeople
{
    NSMutableArray *tempContactsArray = [[NSMutableArray alloc] init];
    
    __block BOOL accessGranted = NO;

    ABAddressBookRef addressBook = ABAddressBookCreate();
    dispatch_semaphore_t semaphore = dispatch_semaphore_create(0);
    ABAddressBookRequestAccessWithCompletion(addressBook, ^(bool granted, CFErrorRef error) {
        accessGranted = granted;
        dispatch_semaphore_signal(semaphore);
    });
    
    dispatch_semaphore_wait(semaphore, DISPATCH_TIME_FOREVER);
    
    if (accessGranted) {
        CFArrayRef allPeople = ABAddressBookCopyArrayOfAllPeople(addressBook);
        CFIndex nPeople = ABAddressBookGetPersonCount(addressBook);
        
        for (int i=0; i < nPeople; i++) {
            
            ABRecordRef ref = CFArrayGetValueAtIndex(allPeople, i);
    
            CFStringRef tempFirstName = ABRecordCopyValue(ref, kABPersonFirstNameProperty);
            CFStringRef tempLastName = ABRecordCopyValue(ref, kABPersonLastNameProperty);
            
            NSString *firstName = (NSString *)CFBridgingRelease(tempFirstName);
            NSString *lastName = (NSString *)CFBridgingRelease(tempLastName);
            
            if (!firstName || [firstName isEqualToString:@"(null)"]) {
                firstName = @"";
            }
            if (!lastName || [lastName isEqualToString:@"(null)"]) {
                lastName = @"";
            }
            
            NSString *name = [NSString stringWithFormat:@"%@%@",lastName,firstName];
            
            ABMutableMultiValueRef tempEmails = ABRecordCopyValue(ref, kABPersonEmailProperty);
            NSString *email = @"";
            if (ABMultiValueGetCount(tempEmails) > 0) {
                
                CFStringRef tempEmail0 = ABMultiValueCopyLabelAtIndex(tempEmails, 0);
                email = (NSString *)CFBridgingRelease(tempEmail0);
                
            }
            
            ABMultiValueRef phones = (__bridge ABMultiValueRef)((__bridge NSString *)ABRecordCopyValue(ref, kABPersonPhoneProperty));
            CFIndex phoneIndex = ABMultiValueGetCount(phones);
            
            NSMutableArray *nameArray = [[NSMutableArray alloc] init];
            
            NSString *nameKey = [self chinesePinYinFirstLetter:name];
            
            for (int j = 0; j<phoneIndex; j++) {
                CFStringRef tempPhoneNumber = ABMultiValueCopyValueAtIndex(phones, j);
                
                NSString *phoneNumber = (NSString *)CFBridgingRelease(tempPhoneNumber);
                
                if ([phoneNumber isEqualToString:@"null"]) {
                    phoneNumber = @"";
                }
                
                if (phoneNumber.length == 0) {
                    continue;
                }
                NSCharacterSet *whiteSpace = [NSCharacterSet whitespaceAndNewlineCharacterSet];
                phoneNumber = [phoneNumber stringByTrimmingCharactersInSet:whiteSpace];
                
                phoneNumber = [self deleteMinus:phoneNumber];
                phoneNumber = [self deletePlus:phoneNumber];
                
                if ([phoneNumber hasPrefix:@"86"] && [phoneNumber length] == 13) {
                    phoneNumber = [phoneNumber substringFromIndex:2];
                }else if ([phoneNumber length] == 11 && [phoneNumber characterAtIndex:0] == '1'){
                    
                }else {
                   // phoneNumber = @"";
                }
                
                if (!phoneNumber || phoneNumber.length == 0) {
                    continue;
                }
                
                ContactPeopleInfoDTO *peopleInfo = [[ContactPeopleInfoDTO alloc] init];
                peopleInfo.name = name;
                peopleInfo.phone = phoneNumber;
                peopleInfo.email = email;
                [nameArray addObject:peopleInfo];
            }
            __block BOOL isKeyExist = NO;

            [tempContactsArray enumerateObjectsUsingBlock:^(id  _Nonnull obj, NSUInteger idx, BOOL * _Nonnull stop) {
                
                NSString *existNameKey = [obj allKeys][0];
                if ([nameKey isEqualToString:existNameKey]) {
                    NSMutableArray *existnameArray = [[NSMutableArray alloc] initWithArray:[obj allValues][0]];
                    [existnameArray addObjectsFromArray:nameArray];
                    [obj setObject:existnameArray forKey:nameKey];
                    isKeyExist = YES;
                    *stop = YES;
                }
            }];
            
            if (!isKeyExist) {
    
                NSMutableDictionary *nameGroupDic = [[NSMutableDictionary alloc] init];
                NSArray *tempNameArray = [NSArray arrayWithArray:nameArray];
                [nameGroupDic setObject:tempNameArray forKey:nameKey];
                [tempContactsArray addObject:nameGroupDic];
            }
        }
        
        [tempContactsArray sortUsingComparator:^NSComparisonResult(id  _Nonnull obj1, id  _Nonnull obj2) {
            NSString *key1 = [obj1 allKeys][0];
            NSString *key2 = [obj2 allKeys][0];
            
            return [key1 compare:key2];
        }];
    }
    return tempContactsArray;
}


+ (NSString *)deleteMinus:(NSString *)originalString
{
    NSCharacterSet *specialCharacters = [NSCharacterSet characterSetWithCharactersInString:@"-"];
    NSString *filterStr = [originalString stringByTrimmingCharactersInSet:specialCharacters];
    
    return filterStr;
}
+ (NSString *)deletePlus:(NSString *)originalString
{
    NSCharacterSet *specialCharacters = [NSCharacterSet characterSetWithCharactersInString:@"+"];
    NSString *filterStr = [originalString stringByTrimmingCharactersInSet:specialCharacters];
    
    return filterStr;
}
+ (NSString *)chinesePinYinAllWordFirstLetter:(NSString *)chinese
{
    NSString *wordsFirstLetters = @"";
    if( !chinese || 0 == [chinese length] ) {
        return @"#";
    }
    
    for (int i = 0; i < [chinese length]; i++) {
        int cLetter = [chinese characterAtIndex:i];
        if( (cLetter > 64 &&  cLetter < 91 ) || (cLetter > 96 &&  cLetter < 123 ) )
        {
        }
        else
            cLetter = chinesePinYinFirstLetter((unsigned short)cLetter);
        
        if( cLetter > 95 )
            cLetter -= 32;
        wordsFirstLetters = [wordsFirstLetters stringByAppendingFormat:@"%c",cLetter] ;
    }
    return wordsFirstLetters;
}
+ (NSString *)chinesePinYinFirstLetter:(NSString *)chinese
{
    if( !chinese || 0 == [chinese length] ) {
       return @"#";
    }
    
    int cLetter = [chinese characterAtIndex:0];
    
    if( !(cLetter > 64 &&  cLetter < 91 ) || !(cLetter > 96 &&  cLetter < 123 ) ){
        cLetter = chinesePinYinFirstLetter((unsigned short)cLetter);
    }
    if (cLetter > 95) {
        cLetter = cLetter - 32;
    }
    return [NSString stringWithFormat:@"%c",cLetter];
}
